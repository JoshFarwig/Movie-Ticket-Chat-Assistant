import java.time; 
import java.util.*; 

public class Customer { 

    // attributes

    private String name; // Name of Customer
    private UUID customerID; // ID of Customer
    private String gender; // Gender of Customer
    private LocalDateTime birthDate; // BirthDate of Customer
    private String email; // email of Customer 

    // class constructor

    public Customer(String name, UUID customerID, String gender, LocalDateTime birthDate, String email) {
        this.name = name; 
        this.customerID = customerID;
        this.gender = gender; 
        this.birthDate = birthDate; 
        this.email = email; 
    } 

    // other Methods 
        



}